/** –––––––––––––––––––––––––––––
 * `shift()` method in JavaScript
 * ––––––––––––––––––––––––––––––
 * @description
 *  > remove first element from an array
 *  > return removed element
 * 
 * @syntax
 *  shift()
 * 
 */
let data = [1, 2, 3, 4, 5];
data.shift(); // remove

console.log(data);
// [ 2, 3, 4, 5]

console.log(data.shift()); // return removed element
// return 2