/** ------------------------
 * `lastIndexOf()` method JS
 * -------------------------
 *    returns the last index at which given element can be found in the array
 *    -1 if it is not present
 *    array is searched backwards, starting at fromIndex
 * 
 *    searching from last
 */

const drinks = ['tea', 'coffee', 'lemon', 'water', 'greentea'];

console.log(drinks.lastIndexOf('tea')); // 0

console.log(drinks.lastIndexOf('coffee')); // 1

console.log(drinks.lastIndexOf('water')); // 3



