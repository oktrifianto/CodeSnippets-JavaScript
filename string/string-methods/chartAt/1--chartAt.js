/** -----------------
 * `charAt` method JS
 * ------------------
 * 
 * @description
 *    returns a new string consisting of the single UTF-16 code unit located at the specified offset into the string
 * 
 */

// --------  example ---------
const s = 'Hello you!';

const index = 4;

console.log(`The character at index ${index} is ${s.charAt(index)}`);
// The character at index 4 is o